    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(username.getText().equals("") || password.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Empty Feild !!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
                  String sql ="select * from temporary_user where user_name =?  and password = ? ";
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/lib_system?serverTimezone=UTC","root","");
                ResultSet rs ;
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, username.getText());
                pstmt.setString(2, password.getText());
                rs = pstmt.executeQuery(); 
                if(rs.next()){
                    student s = new student();
                    s.setVisible(true);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Invalied Username or Password", "Error", JOptionPane.ERROR_MESSAGE);
                }
                }catch(Exception e){
                        JOptionPane.showMessageDialog(null, e);
                }
                
        }
    }//GEN-LAST:event_jButton1ActionPerformed

